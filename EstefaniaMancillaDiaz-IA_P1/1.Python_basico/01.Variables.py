# Ejemplo 1: guardar un numero en una variable son de tipo entero (int)
numero = 10   # aqui "numero" es el nombre de la variable y guarda el valor 10
print("El valor de numero es:", numero)
# print(numero)-- esta es otra forma de mandar a llamar solo la varible 

# Ejemplo 2: guardar texto en una variable son de tipo (string)  
nombre = "fany"   # entre comillas se escribe un texto 
print("Hola, mi nombre es:", nombre)

# Ejemplo 3: guardar un valor decimal son de tipo (float)
pi = 3.1416   # los decimales en python son flotantes, quiere decir con decimales.
print("El valor de pi es:", pi)

# Ejemplo 4: asignar varios valores a varias variables
a, b, c = 1, 2, 3   # Se asignan en orden
print("a:", a, "b:", b, "c:", c) #siempre debe ponerse despues de la (,)
#la variable a llamar o tambien en orden como a,b,c.

